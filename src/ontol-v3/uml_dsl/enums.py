"""
Перечислимые типы (enumerations), используемые в моделях UML DSL.

Все значения подкреплены цитатами из книги «Моделирование на UML» (Новиков, Иванов),
разделы 3.1–3.3.
"""
from __future__ import annotations
from enum import Enum


class Visibility(str, Enum):
    """Видимость составляющей классификатора (§3.1.3, §3.2.2).

    Цитата: «Видимость (visibility) определяет, может ли составляющая одного
    классификатора (в том числе имя) использоваться в другом классификаторе.»

    Цитата: «Ещё раз подчеркнем, что если видимость не указана, то никакого
    значения видимости по умолчанию не подразумевается.»
    """
    PUBLIC = "+"       # «открытый (обозначается знаком + или ключевым словом public)»
    PRIVATE = "-"      # «закрытый (обозначается знаком – или ключевым словом private)»
    PROTECTED = "#"    # «защищенный (обозначается знаком # или ключевым словом protected)»
    PACKAGE = "~"      # «пакетный (обозначается знаком ~ или ключевым словом package)»


class Scope(str, Enum):
    """Область действия составляющей классификатора (§3.1.3).

    Цитата: «Область действия (scope) определяет, как проявляет себя
    составляющая классификатора в экземплярах, т.е. имеют экземпляры свои
    значения составляющей или совместно используют одно значение.»

    - instance: «никак специально не обозначается, поскольку подразумевается по умолчанию»
    - classifier: «описание составляющей классификатора подчеркивается»
    """
    INSTANCE = "instance"
    CLASSIFIER = "classifier"


class Changeability(str, Enum):
    """Изменяемость атрибута или полюса ассоциации (§3.2.2, §3.3.7).

    Цитата: «Свойство {changeable} ... означает отсутствие ограничений на
    изменение значения.»
    Цитата: «Свойство {readOnly} ... означает, что значение не может быть
    изменено после инициализации.»
    """
    UNRESTRICTED = "unrestricted"   # аналог changeable (UML 2)
    READ_ONLY = "readOnly"          # значение не может быть изменено после инициализации (UML 2)


class ParamDirection(str, Enum):
    """Направление параметра операции (§3.2.3).

    Цитата: «in — входной параметр, значение используется, но не изменяется.
    out — выходной параметр, аргумент — слот, куда операция помещает значение.
    inout — входной и выходной одновременно.
    return — возвращаемое значение.»
    """
    IN = "in"
    OUT = "out"
    INOUT = "inout"
    RETURN = "return"


class Concurrency(str, Enum):
    """Параллелизм операции (§3.2.3).

    Цитата: «{sequential} — не допускает параллельных вызовов.
    {guarded} — параллельные вызовы допускаются, но только один выполняется.
    {concurrent} — допускает произвольное число параллельных вызовов.»
    """
    SEQUENTIAL = "sequential"
    GUARDED = "guarded"
    CONCURRENT = "concurrent"


class CollectionKind(str, Enum):
    """Упорядоченность и уникальность множества на полюсе ассоциации (§3.3.7).

    Цитата (таблица):
    ┌──────────────┬─────────────────┬────────────────────┐
    │              │ Упорядоченное   │ Неупорядоченное    │
    ├──────────────┼─────────────────┼────────────────────┤
    │ Уникальное   │ {ordered}       │ {set} (по умолч.)  │
    │ Неуникальное │ {sequence}      │ {bag}              │
    └──────────────┴─────────────────┴────────────────────┘
    """
    SET = "set"             # неупорядоченное множество без повторов (по умолчанию)
    BAG = "bag"             # неупорядоченное мультимножество (с повторами)
    ORDERED = "ordered"     # упорядоченное множество без повторов
    SEQUENCE = "sequence"   # упорядоченная последовательность с повторами


class Stereotype(str, Enum):
    """Стандартные стереотипы классов (§3.2.1, §3.2.4).

    Цитата: «Для классов определены следующие стандартные стереотипы…»
    """
    ACTOR = "actor"                         # действующее лицо
    AUXILIARY = "auxiliary"                  # вспомогательный класс
    ENUMERATION = "enumeration"             # перечислимый тип данных
    FOCUS = "focus"                         # основной класс
    IMPLEMENTATION_CLASS = "implementationClass"  # реализация класса
    INTERFACE = "interface"                 # «все составляющие абстрактные»
    METACLASS = "metaclass"                 # «экземпляры являются классами»
    PROCESS = "process"                     # активный класс
    # (UML 1-specific stereotypes removed)
    SIGNAL = "signal"                       # класс-сигнал
    STEREOTYPE = "stereotype"               # определение нового стереотипа
    TYPE = "type"                           # тип данных
    DATA_TYPE = "dataType"                  # тип данных
    UTILITY = "utility"                     # «нет экземпляров, служба»


class DependencyStereotype(str, Enum):
    """Стандартные стереотипы зависимостей на диаграмме классов (§3.3.1).

    Цитата: «Для зависимостей определены стереотипы: «bind», «call», «derive»,
    «friend», «instanceOf», «instantiate», «powertype», «refine», «use»…»
    """
    BIND = "bind"           # подстановка параметров шаблона
    CALL = "call"           # вызов операции
    DERIVE = "derive"       # «может быть вычислен по»
    FRIEND = "friend"       # дополнительные права видимости
    INSTANCE_OF = "instanceOf"
    INSTANTIATE = "instantiate"  # создание экземпляров
    POWERTYPE = "powertype"
    REFINE = "refine"       # уточнение
    USE = "use"             # общая зависимость


class AggregationKind(str, Enum):
    """Вид агрегации на полюсе ассоциации (§3.3.5).

    Цитата-агрегация: «Агрегация — это ассоциация‚ которая означает‚ что
    экземпляры класса A входят в состав экземпляра класса B.»
    Цитата-композиция: «Композиция — это ассоциация‚ которая дополнительно
    накладывает более сильные ограничения: часть может входить только в одно
    целое, часть существует, только пока существует целое.»
    """
    NONE = "none"
    AGGREGATION = "aggregation"   # пустой ромб
    COMPOSITION = "composition"   # закрашенный ромб
